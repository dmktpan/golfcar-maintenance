import { NextRequest, NextResponse } from 'next/server';

const EXTERNAL_API_BASE = 'http://golfcar.go2kt.com:8080/api';

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    console.log('üîÑ POST /api/proxy/vehicles/transfer - External API Only');
    console.log('üìù Request body:', JSON.stringify(body, null, 2));
    
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 15000); // 15 second timeout
    
    const response = await fetch(`${EXTERNAL_API_BASE}/vehicles/transfer`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(body),
      signal: controller.signal,
    });

    clearTimeout(timeoutId);
    console.log('üåê External API response status:', response.status);

    if (response.ok) {
      const data = await response.json();
      console.log('‚úÖ External API success');
      return NextResponse.json(data);
    } else {
      console.log('‚ùå External API failed with status:', response.status);
      const errorText = await response.text();
      console.log('‚ùå Error response:', errorText);
      
      return NextResponse.json(
        { 
          success: false, 
          message: `External API failed with status ${response.status}`,
          data: null,
          details: errorText
        },
        { status: response.status }
      );
    }
  } catch (error) {
    console.error('‚ùå Error in vehicles transfer POST proxy:', error);
    return NextResponse.json(
      { success: false, message: 'Failed to transfer vehicles with external API' },
      { status: 500 }
    );
  }
}