import { NextRequest, NextResponse } from 'next/server';

const EXTERNAL_API_BASE = 'http://golfcar.go2kt.com:8080/api';

export async function GET(request: NextRequest) {
  try {
    console.log('üîÑ GET /api/proxy/maintenance - External API Only');
    
    // ‡πÉ‡∏ä‡πâ External API ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô
    console.log('üåê Calling external API...');
    
    // ‡πÄ‡∏û‡∏¥‡πà‡∏° timeout
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 15000); // 15 second timeout
    
    // ‡πÄ‡∏û‡∏¥‡πà‡∏° query parameter ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ç‡∏≠‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• parts ‡∏î‡πâ‡∏ß‡∏¢
    const url = new URL(`${EXTERNAL_API_BASE}/maintenance`);
    url.searchParams.append('include', 'parts');
    
    const response = await fetch(url.toString(), {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
      },
      signal: controller.signal,
    });

    clearTimeout(timeoutId);
    console.log('üåê External API response status:', response.status);

    if (response.ok) {
      const data = await response.json();
      console.log('‚úÖ External API success');
      return NextResponse.json(data);
    } else {
      console.log('‚ùå External API failed with status:', response.status);
      const errorText = await response.text();
      console.log('‚ùå Error response:', errorText);
      
      return NextResponse.json(
        { 
          success: false, 
          message: `External API failed with status ${response.status}`,
          details: errorText
        },
        { status: response.status }
      );
    }
  } catch (error) {
    console.error('‚ùå Error fetching maintenance records:', error);
    return NextResponse.json(
      { 
        success: false, 
        message: 'Failed to fetch maintenance records from external API',
        details: error instanceof Error ? error.message : 'Unknown error'
      },
      { status: 500 }
    );
  }
}

